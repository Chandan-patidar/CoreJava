package JavaNotesByChandan.javadatatypes.javanumber;
// # Integer Types
// byte :-The byte data type can store whole numbers from -128 to 127
public class Primitive {
 public static void main(String[] args){
  byte x = 120;
  System.out.println(x);

  // short :-The short data type can store whole numbers from -32768 to 32767:

  short myNum = 500;
  System.out.println(myNum);

  // int:- The int data type can store whole numbers from -2147483648 to 2147483647.

  int y= 100000;
  System.out.println(y);

  // lond:- The long data type can store whole numbers from -9223372036854775808 to 9223372036854775807
  long z= 1234566789L;
  System.out.println(z);

  // # Floating point types :- Represents numbers with a fractional part, containing one or more decimals. There are two types: float and double.

   float a = 93.4394f;
   System.out.print(a);
   double b=15.98d;

   System.out.println(b);

   //






 }
}
