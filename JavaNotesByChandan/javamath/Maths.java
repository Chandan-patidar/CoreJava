package JavaNotesByChandan.javamath;

public class Maths {
    public static void main(String[] args) {
// mathematical task on number
        // Math.max(x,y) :- method can find the highest value of x and y
        System.out.println(Math.max(10, 20));

        //Math.min(x,y) :- method can find the lowest value of x and y
        int y = Math.min(6,9);
        System.out.println(y);

        //Math.sqrt(x) :- method return the square root of X
        Double k =Math.sqrt(64);
        System.out.println(k);

        // Math.abs(x) :- method return the  absolute (Positive) value of x
        System.out.println(Math.abs(-38.0));

        // Math.random() :- return a random number between o.o(inclusive), and 1.o (exclusive)
         System.out.println(Math.random());
    }
}
